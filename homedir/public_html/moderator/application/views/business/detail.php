                        <h2 class="red mt10">وب‌سایت رزرو پزشکی دکترمرکز</h2>
                        <div class="mt50 ultralight line-height2 word-break content-editor">
                            <p style="text-align: justify;">در دورانی که خدمات آنلاین در کسب و کارهای مختلف روز به روز جایگاه خود را در میان عامه مردم پیدا میکند. طراحی سایت &quot;دکتر مرکز&quot; را میتوان نقطه عطفی در زمینه خدمات آنلاین، طراحی سایت پزشکی و به خصوص خدمات آنلاین پزشکی دانست. طراحی وب سایت &quot;دکتر مرکز&quot; شامل بخش ها و مراحل مختلفی در روند طراحی خود بوده است. تیم طراحی سایت فراسو وب با برگزاری جلسات مشاوره متعدد با کارفرمای پروژه، ارائه راهکار ها، پیشنهادات، دریافت نظرات و سلایق کارفرما و اتود های اولیه توانست جمع بندی مناسب و شرح پروژه کاملی از پروژه را فراهم نماید. در طراحی سایت های پزشکی شرح پروژه از اهمیت بالایی برخوردار است.</p>

                            <h3 style="text-align: justify;">طراحی گرافیک سایت پزشکی دکتر مرکز:</h3>

                            <p style="text-align: justify;">در طراحی گرافیک وب سایت دکتر مرکز از آخرین اصول روز در طراحی گرافیک صفحات وب بهره گیری شده است.&nbsp;تیم طراحی گرافیک فراسو وب با مطالعه بر روی سلایق شخصی کارفرما، رنگ های تاثیر گذار در ذهن بیماران و المان های طراحی مرتبط اقدام به طراحی اولیه پروژه نمود که با تعامل کارفرمای محترم این طرح ها نهایی گردید.</p>

                            <h3>بخش های اصلی طراحی سایت دکتر مرکز:</h3>

                            <p style="text-align: justify;">1 &ndash; سیستم عضویت و پروفایل دهی پزشکان: کلیه پزشکان با هر تخصصی میتوانند بعد از گذراندن مراحل عضویت، پروفابل اختصاصی خود را داشته و به عضویت سایت در بیایند، اعضای سایت در پروفایل خود علاوه بر بخش های درج مشخصات، تصاویر، تخصص ها، ارسال مقاله به سایت و شارژ پنل پیامکی، دارای سیستم رزرواسیون پیشرفته پیزشکی میباشند. در طراحی سیستم رزرو پزشکی &quot;دکتر مرکز&quot; ، پزشکان میتوانند با درج زمان های خالی، بیمه های طرف قرارداد و غیره زمانبندی خود را به مشتریان و بیماران ارائه دهند. طراحی سیستم رزرو پزشکی &quot;دکتر مرکز&quot; به صورت اختصاصی توسط تیم طراحی گرافیک و کدنویسی فراسو وب صورت گرفته است.</p>

                            <p style="text-align: justify;">2 &ndash; عاملین فروش: در طراحی سایت &quot;دکتر مرکز&quot; عاملین فروش به اشخاصی گفته میشود که با عضویت در سایت و جذب پزشکان برای عضویت در سایتت میتوانند برای خود کسب درآمد داشته باشند. این اشخاص بعد از عضویت در سایت پروفابل اختصاصی دریافت مینمایند که از طریق آن میتوانند لیست پزشک های جذب شده، درآمد های حاصله از سایت و سایر موارد را مشاهده و ویرایش نمایند، همچنین کد و لینک اختصاصی هریک از این عوامل فروش موجب مشود که به راحتی بتوانند از طریق شبکه های اجتماعی و غیره به جذب پزشکان پرداخته و درآمد مناسبی را برای خود کسب کنند. همچنین پزشکان هنگام عضویت میتوانند کد عامل فروش مورد نظر خود را وارد نمایند.</p>

                            <p style="text-align: justify;">3 &ndash; عضویت بیماران و اعضا عادی: مهمترین بخش و هدف از ارائه خدمات در طراحی وب سایت &quot;دکتر مرکز&quot; ارائه خدمات به بیماران عزیز در سراسر کشور میباشد. در این بخش بیماران با عضویت در سایت میتوانند در سیستم جستجوی پیشرفته سایت پزشکان مورد نظر خود را با توجه به تخصص ها، موقیت مکانی، نام، بیمه و سایر موارد بیابند و با توجه به تزدیکترین زمان اقدام به رزرو وقت از پزشک مورد نظر نمایند. همچنین موقعیت مکانی پزشکان بر روی نقشه قابل مشاهده میباشد.</p>

